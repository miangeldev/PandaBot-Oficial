export async function run(sock, msg, args) {
    const from = msg.key.remoteJid;
    
    if (args.length === 0) {
        let fontList = 'üé® *ESTILOS DE TEXTO DISPONIBLES*\n\n';
        
        Object.entries(fonts).forEach(([key, font]) => {
            fontList += `${key}. ${font.name}\n`;
            fontList += `   Ejemplo: ${font.transform('Hola')}\n\n`;
        });
        
        fontList += 'üí° *Uso:* .styletext <n√∫mero> <texto>\n';
        fontList += 'üìù *Ejemplo:* .styletext 1 Hola Mundo';
        
        return await sock.sendMessage(from, { text: fontList }, { quoted: msg });
    }

    const fontNumber = args[0];
    const text = args.slice(1).join(' ');

    if (!fonts[fontNumber]) {
        return await sock.sendMessage(from, { 
            text: '‚ùå N√∫mero de estilo inv√°lido. Usa .styletext para ver la lista.' 
        }, { quoted: msg });
    }

    if (!text) {
        return await sock.sendMessage(from, { 
            text: '‚ùå Debes proporcionar un texto.' 
        }, { quoted: msg });
    }

    try {
        const styledText = fonts[fontNumber].transform(text);
        
        await sock.sendMessage(from, { 
            text: `${styledText}` 
        }, { quoted: msg });
        
    } catch (error) {
        console.error('Error aplicando estilo:', error);
        await sock.sendMessage(from, { 
            text: '‚ùå Error aplicando el estilo. Intenta con otro texto.' 
        }, { quoted: msg });
    }
}